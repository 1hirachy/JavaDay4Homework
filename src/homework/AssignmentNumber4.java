package homework;

public class AssignmentNumber4 {

	public static void main(String[] args) {

		int number = 7; // we have assigned number value
		boolean isPositive = (number > 0); // we have now assigned isPositive value which is number (7) greater than 0 IE true

		if (number > 0) {			 // Here we are establishing if condition
			number = -100; 			// Reassigning the Number value
		}

		if (isPositive) { 					// here we are establishing condition

			System.out.println("Positive."); // If if condition is true then it will print out this line
		} else {
			System.out.println("Not Positive."); // If the condition is not true than it will print out Not positive

			System.out.println(number); // It will also print this one

		}

	}

}
